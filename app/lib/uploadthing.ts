export type MediaType = 'photos' | 'gif' | 'videos';

export interface Media {
  id: string;
  url: string;
  type: MediaType;
  title: string;
  thumbnail?: string;
}

// Génère un ID unique simple
function generateId(): string {
  return Math.random().toString(36).substring(2, 9);
}

// Transforme les URLs de config en objets Media
export function getAllMedia(): Media[] {
  const media: Media[] = [];

  (Object.keys(uploadthingConfig.media) as MediaType[]).forEach((type) => {
    const entries = uploadthingConfig.media[type];
    entries.forEach((entry, index) => {
      const url = typeof entry === 'string' ? entry : entry.url;
      const title = typeof entry === 'string'
        ? `${type.charAt(0).toUpperCase() + type.slice(1)} ${index + 1}`
        : entry.title;
      media.push({
        id: generateId(),
        url,
        type,
        title,
        thumbnail: type === 'videos' ? url.replace('.mp4', '.jpg') : undefined,
      });
    });
  });

  return media;
}

export function getMediaByType(type: MediaType): Media[] {
  return getAllMedia().filter((media) => media.type === type);
}

export function getMediaById(id: string): Media | undefined {
  return getAllMedia().find((media) => media.id === id);
}

export function getRandomMedia(type?: MediaType): Media {
  const mediaList = type ? getMediaByType(type) : getAllMedia();
  const randomIndex = Math.floor(Math.random() * mediaList.length);
  return mediaList[randomIndex] || getAllMedia()[0];
}
